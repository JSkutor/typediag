"""SKDM 전역 설정 파일.

수식에 사용되는 모든 변수, 가중치, 보정값 상수를 한곳에 모아둔다.
파라미터 튜닝 시 이 파일의 값만 바꾸고 메인 스크립트를 다시 실행하면 된다.
(implementation_plan_SKDM.md / typediag 수학모델.md 참고)

각 값 옆에는 "왜 이 초기값을 골랐는지" 근거를 적어두었다.
실제 사용자 데이터로 검증하며 계속 바뀔 값들이므로 여기서만 수정한다.
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# 1. 키보드 레이아웃 (행 어긋남, U 단위)
# ---------------------------------------------------------------------------
# 표준 키보드 행 어긋남(stagger). 각 행은 이전 행 대비 X축으로 누적 이동한다.
# 이 값들은 임의로 정한 게 아니라 물리 키보드의 "표준 stagger" 수치다.
#   0행(숫자열): 기준 0.0
#   1행(QWERTY): 숫자열 대비 +0.5U      -> 누적 0.50U
#   2행(ASDF):  QWERTY 대비 +0.25U     -> 누적 0.75U
#   3행(ZXCV):  ASDF 대비 +0.5U        -> 누적 1.25U
ROW_STAGGER_U: dict[int, float] = {
    0: 0.00,  # number row
    1: 0.50,  # qwerty
    2: 0.75,  # asdf
    3: 1.25,  # zxcv
}

# 1 Unit 의 실제 좌표 크기(키 한 칸 간격). 좌표 스케일 기준값.
# 1.0 으로 두면 "열 인덱스 = X 좌표"가 되어 직관적이라 기본값으로 선택.
KEY_UNIT: float = 1.0

# 행 간 세로 간격(Y). 키 한 칸은 보통 가로세로가 같은 정사각형에 가까우므로
# 가로 간격(KEY_UNIT)과 동일하게 1.0 으로 둔다.
ROW_HEIGHT_U: float = 1.0


# ---------------------------------------------------------------------------
# 2. theta (각도) 정의용 행 shift
# ---------------------------------------------------------------------------
# 같은 행 벡터의 theta 중복을 피하기 위한 인위적 세로 shift.
# "손은 중간열(ASDF) 살짝 아래에 놓인다"는 직관 활용: 1,2행은 내리고 3행은 올린다.
# 0.25U 는 행 간격(1.0U)의 1/4 로, 행 순서를 흐트러뜨리지 않으면서
# 같은 행 키들의 각도 중복만 풀어줄 정도의 작은 값이라 선택.
THETA_ROW_SHIFT_U: dict[int, float] = {
    0: -0.25,
    1: -0.25,
    2: -0.25,
    3: +0.25,
}

# theta 를 [0, 360) 으로 완전 정규화(균일화)할지 여부.
# True 면 실제 거리/각도와 무관하게 키들을 각도상 균등 배치한다.
# (손가락은 독립적이라 실제 각도 거리가 의미 없다는 판단 -> 균일화 채택)
NORMALIZE_THETA: bool = True


# ---------------------------------------------------------------------------
# 3. z (지연시간) 처리 - 시그모이드
# ---------------------------------------------------------------------------
# 지연시간(ms)을 시그모이드에 통과시키기 전 자르는(clip) 범위.
# 이제 하한은 0.0ms로 고정하고, 상한은 사용자별/세션별로 컷오프(이상치 제외 최댓값)에 맞춤 설정됩니다.
# 시그모이드의 중심(CENTER)과 기울기(STEEPNESS) 또한 이 상한값에 따라 동적으로 비례 결정됩니다.
# (구현 상세는 src/model.py 의 sigmoid_latency 참고)
# ---------------------------------------------------------------------------


# ---------------------------------------------------------------------------
# 4. 예외처리 / 노이즈 필터링
# ---------------------------------------------------------------------------
# 이상치 제거:
# - 최소(하한) 이상치 제거는 없음.
# - 최대(상한) 이상치: 유의미한 데이터가 모이기 전엔 고정값(1500ms),
#   이후에는 로그 변환 기반 IQR 을 사용하여 동적으로 자름.
OUTLIER_STATIC_MAX_MS: float = 1500.0
OUTLIER_IQR_MIN_EVENTS: int = 200
OUTLIER_IQR_MULTIPLIER: float = 1.5

# 압축 시 해당 키 쌍의 최소 관측 빈도. 미만이면 신뢰도 매우 낮음으로 취급.
# 일단 1 로 두어 모든 관측을 살린다(데이터가 적은 상황 가정).
MIN_FREQUENCY: int = 1


# ---------------------------------------------------------------------------
# 5. 압축 (Weighted Average)
# ---------------------------------------------------------------------------
# 빈도수를 가중치로 사용할 때의 지수. weight = frequency ** POWER.
#   - 1.0(기본): 빈도에 선형 비례. 자주 친 전이가 그만큼 비례해서 대표값을 끈다.
#   - >1.0: 자주 친 전이를 더 강하게 신뢰(편향 강화).
#   - 0.0: 빈도 무시(단순 평균).
# 우선 가장 중립적인 선형(1.0)에서 시작해 튜닝한다.
FREQUENCY_WEIGHT_POWER: float = 1.0


# ---------------------------------------------------------------------------
# 6. 메시화 / 신뢰도 전파 (Graph Laplacian)
# ---------------------------------------------------------------------------
# 라플라시안 평활화 강도(0~1). 0 이면 전파 없음, 클수록 주변값에 끌려감.
# 0.2 는 "원래 값 80% + 주변 평균 20%" 정도의 약한 평활화. 데이터를 뭉개지 않으면서
# 빈 키/저신뢰 키를 주변값으로 메우는 정도라 보수적 초기값으로 선택.
LAPLACIAN_SMOOTHING_ALPHA: float = 0.2

# 평활화 반복 횟수. 횟수가 늘수록 전파가 더 멀리 퍼진다.
# 2 회 정도면 바로 옆 + 그 옆 정도까지만 부드럽게 번져 과전파를 피한다.
LAPLACIAN_ITERATIONS: int = 2


# ---------------------------------------------------------------------------
# 7. 시각화
# ---------------------------------------------------------------------------
# Plotly colorscale 및 출력 옵션.
COLORSCALE: str = "Viridis"
RENDER_OPEN_BROWSER: bool = True
OUTPUT_HTML_PATH: str = "skdm_surface.html"


# ---------------------------------------------------------------------------
# 8. 분석 제외 행
# ---------------------------------------------------------------------------
# 분석(집계/요약/메시화/평활화/렌더링)에서 통째로 빼는 행 인덱스 집합.
# 숫자열(0행)은 타자 연습 입력으로 들어오지 않으므로 분석 대상이 아니다.
# layout.py 에는 좌표 기준(행 stagger 계산용)으로 남겨두지만, 파이프라인은
# 여기 지정된 행의 키를 결과(노드)로 만들지 않는다.
#   - 만약 분석에 포함했다면: 데이터가 없어 z=0·신뢰도=0 인 '죽은 점'이 되어
#     이웃 키들을 0 쪽으로 끌어내려 면을 왜곡시킨다. 그래서 아예 제외한다.
EXCLUDE_ROWS: set[int] = {0}
